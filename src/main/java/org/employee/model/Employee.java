package org.employee.model;

import java.sql.Date;
import java.sql.Timestamp;

public class Employee {

    private int id;
    private String first_name;
    private String last_name;
    private String email;
    private String phone;
    private String department;
    private double salary;
    private Date joining_date;
    private String status;
    private Timestamp created_at;
    private Timestamp updated_at;

    public Employee() {
    }

    public Employee(int id, String first_name, String last_name, String email, String phone, String department, double salary, Date joining_date, String status, Timestamp created_at, Timestamp updated_at) {
        this.id = id;
        this.first_name = first_name;
        this.last_name = last_name;
        this.email = email;
        this.phone = phone;
        this.department = department;
        this.salary = salary;
        this.joining_date = joining_date;
        this.status = status;
        this.created_at = created_at;
        this.updated_at = updated_at;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }

    public Date getJoining_date() {
        return joining_date;
    }

    public void setJoining_date(Date joining_date) {
        this.joining_date = joining_date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Timestamp getCreated_at() {
        return created_at;
    }

    public void setCreated_at(Timestamp created_at) {
        this.created_at = created_at;
    }

    public Timestamp getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(Timestamp updated_at) {
        this.updated_at = updated_at;
    }

    @Override
    public String toString() {
        return String.format(
                "Employee{id=%d, name=%s %s, email=%s, dept=%s, salary=%.2f, status=%s}",
                id, first_name, last_name, email, department, salary, status
        );
    }
}
